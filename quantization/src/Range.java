
public class Range {
	private int min;
	private int max;
	private int q;
	private int s;
	
	
	public int getMin() {
		return min;
	}
	public void setMin(int min) {
		this.min = min;
	}
	public int getMax() {
		return max;
	}
	public void setMax(int max) {
		this.max = max;
	}
	public int getQ() {
		return q;
	}
	public void setQ(int q) {
		this.q = q;
	}
	public int getS() {
		return s;
	}
	public void setS(int s) {
		this.s = s;
	}
}
